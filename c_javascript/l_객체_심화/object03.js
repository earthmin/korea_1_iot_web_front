// 메서드
// 1) Math.random()
// : 0 이상 1미만의 난수 생성 
console.log(Math.random());
console.log(Math.random() * 10);

// 2) Math.floor()
// : 숫자를 가장 가까운 정수로 내림 
console.log(Math.floor(Math.random() * 10)); //0부터 9까지의 정수

// 3) Math.sqrt() // square root
console.log(Math.sqrt(16));
console.log(Math.sqrt(144));
